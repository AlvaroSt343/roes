<br />
Por su parte, las web beacons son imágenes insertadas en una página de Internet o correo electrónico, que puede ser utilizado para monitorear el comportamiento de un visitante, como almacenar información sobre la dirección IP del usuario, duración del tiempo de interacción en dicha página y el tipo de navegador utilizado, entre otro Esta información se usa para mejorar nuestros servicios. Los contadores de visita normalmente no recopilan información diferente de la que su navegador nos proporciona como parte estándar de cualquier comunicación por internet.
Le informamos que utilizamos cookies y web beacons para obtener información personal (ROES & CO., no recaba información sensible) de usted, como la siguiente con la finalidad de permitir la identificación anónima de los usuarios (identifica navegadores y dispositivos, no personas) y por lo tanto la contabilización aproximada del número de visitantes y su tendencia en el tiempo.<br />
Su tipo de navegador y sistema operativo.<br />
Las páginas de Internet que visita.<br />
Los vínculos que sigue y los contenidos más visitados (Dicha identificación se realiza de forma anónima)<br />
El sitio que visitó antes de entrar al nuestro.<br />
<br />
Importante: salvo que el usuario decida registrarse en un Servicio del Portal, la cookie nunca irá asociada a ningún dato de carácter personal que pueda identificarle. Dichas cookies sólo serán utilizadas con propósitos estadísticos que ayuden a la optimización de la experiencia de los usuarios en la Web.
